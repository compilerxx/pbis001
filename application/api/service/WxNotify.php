        //return parent::NotifyProcess($data, $msg); // TODO: Change the autogenerated stub

        if ($data['result_code' =='SUCCESS']){ //$data 已被转成数组，见NotifyProcess 方法的参数说明

            $orderNo = $data['out_trade_no'];
            Db::startTrans();
            try{
                $order = OrderModel::where('order_no','=',$orderNo)->find();

                if ($order->status ==1){
                    $service = new OrderService();
                    $stockStatus = $service->checkOrderStock($order->id);
                    if ($stockStatus['pass']){
                        $this->updateOrderStatus($order->id,true);
                        $this->reduceStock($stockStatus);
                    }else{
                        $this->updateOrderStatus($order->id,false);
                    }
                }
                Db::commit();
                return true; //见父方法 NotifyProcess 说明。
            }catch (Exception $ex){
                Db::rollback();
                Log::record($ex);
                return false;
            }
        }else{
            return false;
        }


    }

    private function reduceStock($stockStatus){
        foreach ($stockStatus['pStatusArray'] as $singlePStatus){
            ProductModel::where('id','=',$singlePStatus['id'])
                ->setDec('stock',$stockStatus['count']); //setDec 可以对某个field 做减法

        }
    }

    private function updateOrderStatus($orderID,$success){
        $status = $success?
            OrderStatusEnum::PAID :
            OrderStatusEnum::PAID_BUT_OUT_OF;
        OrderModel::where('id','=',$orderID)->update(['status'=>$status]);
    }

}